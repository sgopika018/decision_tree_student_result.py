import pandas as pd
from sklearn.tree import DecisionTreeClassifier
from sklearn.preprocessing import LabelEncoder
data = {
    'Attendance': ['High', 'High', 'Low', 'High', 'Low', 'Low', 'High'],
    'Study_Hours': ['Yes', 'No', 'Yes', 'Yes', 'No', 'Yes', 'No'],
    'Internal_Marks': ['Good', 'Good', 'Poor', 'Good', 'Poor', 'Good', 'Poor'],
    'Result': ['Pass', 'Pass', 'Fail', 'Pass', 'Fail', 'Fail', 'Pass']
}
df = pd.DataFrame(data)
le = LabelEncoder()
for column in df.columns:
    df[column] = le.fit_transform(df[column])
X = df.drop('Result', axis=1)
y = df['Result']
model = DecisionTreeClassifier(criterion='entropy')
model.fit(X, y)
new_student = [[1, 1, 0]]
prediction = model.predict(new_student)
print("Prediction:", "Pass" if prediction[0] == 1 else "Fail")